     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = $request->input();
        $user = new User();
        $user->name = $data->name;
        $user->email = $data->email;
        $user->password = $data->pass;
        $user->save();
        return redirect()->route('posts.index');
    }

